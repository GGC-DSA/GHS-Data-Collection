column_indices = [8,11,14,19,22]

def bin_score(value):
    if value == '1-100':
        return 1
    elif value == '101-300':
        return 2
    elif value == '301-600':
        return 3
    elif value == '601-900':
        return 4
    elif value == '901-950':
        return 5
    else:
        return None

for index in column_indices:
  df.iloc[:,index] = df.iloc[:,index].apply(bin_score)

df['Total_Score'] = df.iloc[:, column_indices].sum(axis=1)

